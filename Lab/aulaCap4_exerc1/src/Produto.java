public class Produto {
    // ATRIBUTOS
    String nome;
    String descricao;
    String fabricante;
    double preco;

    // CONSTRUTOR
    Produto(String nome, String descricao, String fabricante, double preco){
        this.nome = nome;
        this.descricao = descricao;
        this.fabricante = fabricante;
        this.preco = preco;
    }
}
