public class Contato {
    // ATRIBUTOS
    String nome;
    String email;
    String telefone;
    String dataNascimento;

    // CONSTRUTOR
    Contato(String nome, String email, String telefone, String dataNascimento){
        this.nome = nome;
        this.email = email;
        this.telefone = telefone;
        this.dataNascimento = dataNascimento;
    }
}
